#!/usr/bin/env node

/**
 * Generates SQL to import historic bakery order data into Supabase.
 *
 * Usage: node scripts/import-historic-data.js
 * Output: scripts/import-historic.sql
 */

const fs = require('fs');
const path = require('path');

const parsedOrders = JSON.parse(
  fs.readFileSync(path.join(__dirname, 'parsed-orders.json'), 'utf-8')
);

// ── Price lookup (confirmed from raw CSV column headers) ──
// CSV columns per customer row: name, [sd_qty], $11, [foc_reg_qty], $14, [foc_sun_qty], $16,
//   [pizza_qty], $4, [fougasse_qty], $6, [burger_qty], $2.20, [banana_qty], $3.50,
//   [mince/butterbean_qty], $4/$10, [tarte_qty], $60/$65
//
// The $60/$65 in the last price column is NOT the Tarte au Citron per-unit price — it appears
// to be a column header artifact. Tarte au Citron at $4 per slice is in column index 16.
// Butter Bean Dip replaces Mince Pies in the 6 Feb week at $10 (column index 15).
// Hot X Buns (20 March): 6 buns for $10 → sold as pack of 6.

const PRICE_MAP = {
  'Sourdough': 11.00,
  'Focaccia': 14.00,
  'Focaccia - Regular (toms + Garlic)': 14.00,
  'Focaccia - Regular (toms, Garlic, Rose)': 14.00,
  'Focaccia Regular': 14.00,
  'Focaccia Sun Toms + Olives': 16.00,
  'Pizza Dough': 4.00,
  'Fougasse': 6.00,
  'Burger Buns': 2.20,
  'Banana Choc Muffins': 3.50,
  'Mince Pies': 3.50,
  'Tarte au Citron': 60.00,   // whole tarte — Geraldine: 10 burger buns ($22) + 1 tarte = $82 → tarte = $60
  'Butter Bean Dip': 10.00,
  'Hot X Buns': 10.00,        // pack of 6 for $10
};

// unit_label overrides (default is "each")
const UNIT_LABEL_MAP = {
  'Hot X Buns': 'pack of 6',
};

// Products where parsed quantity needs dividing to get pack count
// Hot X Buns: parsed qty is individual buns, but sold as packs of 6
const QTY_DIVISOR = {
  'Hot X Buns': 6,
};

// ── Week date mapping ──
// Format: { weekName: { opens_at, closes_at, collection_date } }
// opens_at = Monday of that week, closes_at = Thursday evening
const WEEK_DATES = {
  '9 January 2026':    { opens: '2026-01-05', closes: '2026-01-08', collect: '2026-01-09' },
  '16 January 2026':   { opens: '2026-01-12', closes: '2026-01-15', collect: '2026-01-16' },
  '23 January 2026':   { opens: '2026-01-19', closes: '2026-01-22', collect: '2026-01-23' },
  '30 January 2026':   { opens: '2026-01-26', closes: '2026-01-29', collect: '2026-01-31' }, // Saturday
  '6 February 2026':   { opens: '2026-02-02', closes: '2026-02-05', collect: '2026-02-06' },
  '13 February 2026':  { opens: '2026-02-09', closes: '2026-02-12', collect: '2026-02-13' },
  '20 March 2026':     { opens: '2026-03-16', closes: '2026-03-19', collect: '2026-03-20' },
};

// ── Collect unique customers ──
const customerSet = new Map(); // name -> { is_on_account }
for (const week of parsedOrders.weeks) {
  for (const order of week.orders) {
    const name = order.customer_name;
    if (!customerSet.has(name)) {
      customerSet.set(name, {
        is_on_account: (order.status || '').toLowerCase().includes('on account'),
      });
    }
    // If any order is on_account, mark the customer
    if ((order.status || '').toLowerCase().includes('on account')) {
      customerSet.get(name).is_on_account = true;
    }
  }
}

// Helper: get the actual quantity for DB (apply divisor for pack products)
function getDbQuantity(productName, parsedQty) {
  const divisor = QTY_DIVISOR[productName];
  if (divisor) {
    return Math.round(parsedQty / divisor);
  }
  return parsedQty;
}

// ── Verify prices against order totals ──
// Quick sanity check (logged, not blocking)
for (const week of parsedOrders.weeks) {
  for (const order of week.orders) {
    let computed = 0;
    for (const item of order.items) {
      const price = PRICE_MAP[item.product];
      if (price === undefined) {
        console.warn(`WARNING: No price for product "${item.product}" in week "${week.name}"`);
        continue;
      }
      const dbQty = getDbQuantity(item.product, item.quantity);
      computed += price * dbQty;
    }
    const reported = parseFloat((order.total || '$0').replace('$', ''));
    if (Math.abs(computed - reported) > 0.01) {
      console.warn(
        `MISMATCH: ${order.customer_name} in "${week.name}": computed=$${computed.toFixed(2)} vs reported=$${reported.toFixed(2)}`
      );
    }
  }
}

// ── Generate SQL ──
const lines = [];
const emit = (s) => lines.push(s);

emit('-- ==========================================================');
emit('-- Historic data import for Eli\'s Artisan Bakery');
emit('-- Generated by scripts/import-historic-data.js');
emit(`-- Generated at: ${new Date().toISOString()}`);
emit('-- ==========================================================');
emit('');
emit('BEGIN;');
emit('');

// We'll use a DO block with temp tables to hold generated IDs.
// Strategy:
//   1. Create temp tables to map our labels to generated UUIDs
//   2. Insert weeks, capture IDs
//   3. Insert products, capture IDs
//   4. Insert customers, capture IDs
//   5. Insert orders + order_items

emit('-- Temp tables for ID mapping');
emit('CREATE TEMP TABLE _week_map (label TEXT PRIMARY KEY, id UUID NOT NULL);');
emit('CREATE TEMP TABLE _product_map (week_label TEXT NOT NULL, product_name TEXT NOT NULL, id UUID NOT NULL, PRIMARY KEY (week_label, product_name));');
emit('CREATE TEMP TABLE _customer_map (name TEXT PRIMARY KEY, id UUID NOT NULL);');
emit('CREATE TEMP TABLE _order_map (week_label TEXT NOT NULL, customer_name TEXT NOT NULL, id UUID NOT NULL, PRIMARY KEY (week_label, customer_name));');
emit('');

// ── 1. Insert weeks ──
emit('-- ============================================================');
emit('-- WEEKS');
emit('-- ============================================================');
for (const week of parsedOrders.weeks) {
  const dates = WEEK_DATES[week.name];
  const label = `Week of ${week.name}`;
  const collectionDay = week.collection_day;
  const escapedLabel = label.replace(/'/g, "''");

  emit(`INSERT INTO _week_map (label, id)`);
  emit(`SELECT '${escapedLabel}', id FROM (`);
  emit(`  INSERT INTO week (label, status, collection_days, opens_at, closes_at)`);
  emit(`  VALUES (`);
  emit(`    '${escapedLabel}',`);
  emit(`    'closed',`);
  emit(`    '{${collectionDay}}',`);
  emit(`    '${dates.opens}T08:00:00+11:00'::timestamptz,`);
  emit(`    '${dates.closes}T18:00:00+11:00'::timestamptz`);
  emit(`  )`);
  emit(`  RETURNING id`);
  emit(`) AS ins;`);
  emit('');
}

// ── 2. Insert products ──
emit('-- ============================================================');
emit('-- PRODUCTS');
emit('-- ============================================================');
for (const week of parsedOrders.weeks) {
  const weekLabel = `Week of ${week.name}`;
  const escapedWeekLabel = weekLabel.replace(/'/g, "''");

  for (let i = 0; i < week.products.length; i++) {
    const product = week.products[i];
    const price = PRICE_MAP[product.name];
    if (price === undefined) {
      console.warn(`WARNING: No price for "${product.name}" — skipping`);
      continue;
    }
    const unitLabel = UNIT_LABEL_MAP[product.name] || 'each';
    const escapedName = product.name.replace(/'/g, "''");

    emit(`INSERT INTO _product_map (week_label, product_name, id)`);
    emit(`SELECT '${escapedWeekLabel}', '${escapedName}', id FROM (`);
    emit(`  INSERT INTO product (week_id, name, price, unit_label, display_order)`);
    emit(`  VALUES (`);
    emit(`    (SELECT id FROM _week_map WHERE label = '${escapedWeekLabel}'),`);
    emit(`    '${escapedName}',`);
    emit(`    ${price.toFixed(2)},`);
    emit(`    '${unitLabel}',`);
    emit(`    ${i}`);
    emit(`  )`);
    emit(`  RETURNING id`);
    emit(`) AS ins;`);
    emit('');
  }
}

// ── 3. Insert customers ──
// whatsapp_number has a UNIQUE constraint, so we use a unique placeholder per customer
emit('-- ============================================================');
emit('-- CUSTOMERS');
emit('-- ============================================================');
let customerIdx = 0;
for (const [name, info] of customerSet) {
  const escapedName = name.replace(/'/g, "''");
  // Use a unique placeholder for whatsapp since it has a UNIQUE constraint
  // We'll use 'historic_N' pattern so they can be updated later
  const placeholder = `historic_${String(customerIdx).padStart(3, '0')}`;
  customerIdx++;

  emit(`INSERT INTO _customer_map (name, id)`);
  emit(`SELECT '${escapedName}', id FROM (`);
  emit(`  INSERT INTO customer (name, whatsapp_number, is_on_account)`);
  emit(`  VALUES ('${escapedName}', '${placeholder}', ${info.is_on_account})`);
  emit(`  ON CONFLICT (whatsapp_number) DO NOTHING`);
  emit(`  RETURNING id`);
  emit(`) AS ins;`);
  emit('');
}

// ── 4. Insert orders ──
emit('-- ============================================================');
emit('-- ORDERS');
emit('-- ============================================================');
for (const week of parsedOrders.weeks) {
  const weekLabel = `Week of ${week.name}`;
  const escapedWeekLabel = weekLabel.replace(/'/g, "''");

  for (const order of week.orders) {
    const escapedCustomer = order.customer_name.replace(/'/g, "''");
    const status = (order.status || '').trim().toLowerCase();

    let paymentStatus;
    let notes = null;
    if (status === 'paid') {
      paymentStatus = 'paid';
    } else if (status === 'on account') {
      paymentStatus = 'on_account';
    } else if (status && status !== '') {
      paymentStatus = 'pending';
      notes = order.status.trim().replace(/'/g, "''");
    } else {
      paymentStatus = 'pending';
    }

    emit(`INSERT INTO _order_map (week_label, customer_name, id)`);
    emit(`SELECT '${escapedWeekLabel}', '${escapedCustomer}', id FROM (`);
    emit(`  INSERT INTO "order" (week_id, customer_id, collection_day, payment_status, notes)`);
    emit(`  VALUES (`);
    emit(`    (SELECT id FROM _week_map WHERE label = '${escapedWeekLabel}'),`);
    emit(`    (SELECT id FROM _customer_map WHERE name = '${escapedCustomer}'),`);
    emit(`    '${week.collection_day}',`);
    emit(`    '${paymentStatus}',`);
    emit(`    ${notes ? `'${notes}'` : 'NULL'}`);
    emit(`  )`);
    emit(`  RETURNING id`);
    emit(`) AS ins;`);
    emit('');
  }
}

// ── 5. Insert order items ──
emit('-- ============================================================');
emit('-- ORDER ITEMS');
emit('-- ============================================================');
for (const week of parsedOrders.weeks) {
  const weekLabel = `Week of ${week.name}`;
  const escapedWeekLabel = weekLabel.replace(/'/g, "''");

  for (const order of week.orders) {
    const escapedCustomer = order.customer_name.replace(/'/g, "''");

    for (const item of order.items) {
      const escapedProduct = item.product.replace(/'/g, "''");

      const dbQty = getDbQuantity(item.product, item.quantity);
      emit(`INSERT INTO order_item (order_id, product_id, quantity)`);
      emit(`VALUES (`);
      emit(`  (SELECT id FROM _order_map WHERE week_label = '${escapedWeekLabel}' AND customer_name = '${escapedCustomer}'),`);
      emit(`  (SELECT id FROM _product_map WHERE week_label = '${escapedWeekLabel}' AND product_name = '${escapedProduct}'),`);
      emit(`  ${dbQty}`);
      emit(`);`);
      emit('');
    }
  }
}

// ── Cleanup temp tables ──
emit('-- ============================================================');
emit('-- CLEANUP');
emit('-- ============================================================');
emit('DROP TABLE _order_map;');
emit('DROP TABLE _customer_map;');
emit('DROP TABLE _product_map;');
emit('DROP TABLE _week_map;');
emit('');
emit('COMMIT;');

// ── Write SQL file ──
const outputPath = path.join(__dirname, 'import-historic.sql');
fs.writeFileSync(outputPath, lines.join('\n') + '\n', 'utf-8');
console.log(`\nSQL written to: ${outputPath}`);
console.log(`Total lines: ${lines.length}`);
